from turtle import *
shape("turtle")
pensize(1)
pu()
def triangle_small(x, y):
    color("blue", "blue")
    begin_fill()
    goto(x, y)
    pd()
    fd(50)
    rt(120)
    fd(50)
    rt(120)
    fd(50)
    rt(120)
    end_fill()
    pu()
    rt(60)
    fd(50)
    lt(60)
def square_small(x,y):
    color("yellow", "yellow")
    begin_fill()
    goto(x, y)
    pd()
    fd(50)
    rt(90)
    fd(50)
    rt(90)
    fd(50)
    rt(90)
    fd(50)
    rt(90)
    end_fill()
    pu()
    rt(90)
    fd(50)
    lt(90)
done()